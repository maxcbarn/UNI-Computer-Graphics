"use strict";

let vertexShaderSource = `#version 300 es
    in vec4 a_position;
    in vec4 a_color;
    uniform mat4 u_matrix;

    out vec4 v_color;
    void main() {
        gl_Position = u_matrix * a_position;
        v_color = a_color;
    }`;
    
let fragmentShaderSource = `#version 300 es
    precision highp float;

    in vec4 v_color;

    out vec4 outColor;

    void main() {
        outColor = v_color;
    }`;


function main( ) {
    let canvas = document.querySelector("#canvas");
    let gl = canvas.getContext("webgl2");
    
    let vertexShader = CreateShader(gl, gl.VERTEX_SHADER, vertexShaderSource);
    let fragmentShader = CreateShader(gl, gl.FRAGMENT_SHADER, fragmentShaderSource);
    let program = createProgram(gl, vertexShader, fragmentShader);
    gl.useProgram(program);
    gl.clearColor(0, 0, 0, 0);
    gl.canvas.width = canvas.width;
    gl.canvas.height = canvas.height;
    gl.viewport(0, 0, gl.canvas.width, gl.canvas.height);

    
    let positionLocation = gl.getAttribLocation( program, "a_position" );
    let colorLocation = gl.getAttribLocation( program , "a_color");
    let matrixLocation = gl.getUniformLocation( program , "u_matrix" );

    let quantityOfObjs = 2
    let translationsOrigins = [ [ 100 , 50 , -100 ] , [ 0 , 0 , -150 ] ];
    let moveOrigin = [ m4.translation(-70,-75,0) , m4.translation(-50, -75,0) ];
    let translations = translationsOrigins;
    let rotationsOrigins = [ [ 0 , 90 , 0 ] , [ 0 , 0 , 50 ] ];
    let rotationSpeed =[ [ 0 , 0 , 0 ], [ -7 , 2 , 5 ] ];
    let translationSpeed =[ [ 45 , -45 , 0 ], [ 0 , 0 , 0 ] ];
    let translationLimits = [ [ [ -150 , 150 ] , [ -130 , 130 ] , [ 0 , 0 ] ] , [ [ 0 , 0 ] , [ 0 , 0 ] , [ 0 , 0 ] ] ];
    let rotations = rotationsOrigins;
    let scalesOrigins = [ [ 0.5 , 0.5 , 0.5 ] , [ 0.5 , 0.5 , 0.5 ] ];
    let scales = scalesOrigins;
    
    let positionBuffer = gl.createBuffer();
    let vao = gl.createVertexArray();
    gl.bindVertexArray(vao);
    gl.enableVertexAttribArray(positionLocation);
    gl.bindBuffer(gl.ARRAY_BUFFER, positionBuffer);

    let size = 3;          // 2 components per iteration
    let type = gl.FLOAT;   // the data is 32bit floats
    let normalize = false; // don't normalize the data
    let stride = 0;        // 0 = move forward size * sizeof(type) each iteration to get the next position
    let offset = 0;        // start at the beginning of the buffer
    gl.vertexAttribPointer( positionLocation, size, type, normalize, stride, offset);

    setGeometry(gl);

    let colorBuffer = gl.createBuffer()
    gl.bindBuffer( gl.ARRAY_BUFFER , colorBuffer );
    setColors( gl );
    gl.enableVertexAttribArray(colorLocation);

    size = 3;          // 3 components per iteration
    type = gl.UNSIGNED_BYTE;   // the data is 8bit unsigned bytes
    normalize = true;  // convert from 0-255 to 0.0-1.0
    stride = 0;        // 0 = move forward size * sizeof(type) each
    offset = 0;        // start at the beginning of the buffer
    gl.vertexAttribPointer(
        colorLocation, size, type, normalize, stride, offset);
 
    gl.enable( gl.CULL_FACE )
    gl.enable(gl.DEPTH_TEST);
    let then = 0;

      
      
    let cameraTransform = [ 200 , 0 , 0 ];
    let cameraLimits = [ [ -300 , 300 ] , [ 0 , 0 ] , [ 0 , 0 ] ];
    let cameraSpeed = [ 150 , 0 , 0 ];
    let cameraRotation = [ 0 , 0 , 0 ];
    var up = [0, 1, 0];
    let target = [ 0 , 0 , 1 ];


    
    requestAnimationFrame(DrawScene);

    function DrawScene( now ) {
        let matrix;
        let primitiveType = gl.TRIANGLES;
        let offset = 0;
        let count = 16 * 6;
        gl.clear(gl.COLOR_BUFFER_BIT | gl.DEPTH_BUFFER_BIT);
        gl.useProgram(program);
        gl.bindVertexArray(vao);

        now *= 0.001;
        let deltaTime = now - then;
        then = now;
        
        for (let axis = 0; axis < 3; axis++) {
          if( cameraTransform[axis] + cameraSpeed[axis] * deltaTime <= cameraLimits[axis][0] ) {
            cameraSpeed[axis] *= -1;
          }
          if( cameraTransform[axis] + cameraSpeed[axis] * deltaTime >= cameraLimits[axis][1] ) {
            cameraSpeed[axis] *= -1;
          }
          cameraTransform[axis] += cameraSpeed[axis] * deltaTime;
        }

        let cameraMatrix = m4.identity();
        cameraMatrix = m4.lookAt( cameraTransform , target , up , true );
        cameraMatrix = m4.inverse( cameraMatrix );
        cameraMatrix = m4.multiply( m4.perspective( 70 * 180 / Math.PI , gl.canvas.clientWidth / gl.canvas.clientHeight , 1 , 1000 ) , cameraMatrix )



        for ( let index = 0; index < quantityOfObjs ; index++ ) {
          for (let axis = 0; axis < 3; axis++) {
            rotations[index][axis] = rotationSpeed[index][axis] * deltaTime + rotationsOrigins[index][axis];
            if( translations[index][axis] + translationSpeed[index][axis] * deltaTime <= translationLimits[index][axis][0] ) {
              translationSpeed[index][axis] *= -1;
            }
            if( translations[index][axis] + translationSpeed[index][axis] * deltaTime >= translationLimits[index][axis][1] ) {
              translationSpeed[index][axis] *= -1;
            }
            translations[index][axis] += translationSpeed[index][axis] * deltaTime;
          }

          matrix = cameraMatrix;
          matrix = m4.translate( matrix , translations[index][0], translations[index][1], translations[index][2]);
          matrix = m4.xRotate(matrix, rotations[index][0]);
          matrix = m4.yRotate(matrix, rotations[index][1]);
          matrix = m4.zRotate(matrix, rotations[index][2]);
          matrix = m4.scale(matrix, scales[index][0], scales[index][1], scales[index][2]);
          matrix = m4.multiply( matrix , moveOrigin[index] );
          gl.uniformMatrix4fv(matrixLocation, false, matrix);
          
          gl.drawArrays(primitiveType, offset, count);
        }
        requestAnimationFrame(DrawScene);
    }
}

function CreateShader(gl, type, source) {
  let shader = gl.createShader( type );
  gl.shaderSource(shader, source);
  gl.compileShader(shader);
  let success = gl.getShaderParameter(shader, gl.COMPILE_STATUS);
  if (success) {
    return shader;
  }

  console.log(gl.getShaderInfoLog(shader));
  gl.deleteShader(shader);
}

function createProgram(gl, vertexShader, fragmentShader) {
  let program = gl.createProgram();
  gl.attachShader(program, vertexShader);
  gl.attachShader(program, fragmentShader);
  gl.linkProgram(program);
  let success = gl.getProgramParameter(program, gl.LINK_STATUS);
  if (success) {
    return program;
  }
 
  console.log(gl.getProgramInfoLog(program));
  gl.deleteProgram(program);
}

